;de uma matriz 3X3

.MODEL SMALL
.STACK 100H
.DATA
    MATRIZ DB 3 DUP(3 DUP(?))
    MSG1 DB "DIGITE OS ELEMENTOS DA DIAGONAL PRINCIPAL: $"
    SUM DB ?
.CODE


MAIN PROC
MOV AX, @DATA
MOV DS, AX

MOV CX, 3
XOR BX, BX
XOR SI, SI

MOV AH, 9 
LEA DX, MSG1
INT 21H

MOV AH, 1

LER: 
INT 21H
SUB AL, 30H

MOV MATRIZ[BX][SI], AL
ADD SUM, AL 

ADD BX, 3
INC SI

LOOP LER

MOV AH, 2
MOV DL, 10 
INT 21H

IMPRIMIR_MATRIZ:
XOR SI, SI 
XOR BX, BX 

MOV CL, 3
MOV CH, 3
MOV AH, 02H

LINHA:
MOV DL, MATRIZ[BX + SI]
ADD DL, 30H
INT 21H

INC SI
CMP SI, 3
JE COLUNA
JMP LINHA

COLUNA:
MOV DL, 10
INT 21H

XOR SI,SI 
ADD BX, 3
CMP BX, 9
JE SAI
JMP LINHA

SAI:

ADD SUM, 30H

MOV AH, 2

MOV DL, 10
INT 21H
MOV DL, SUM
INT 21H

MOV AH, 4CH
INT 21H


MAIN ENDP
END MAIN