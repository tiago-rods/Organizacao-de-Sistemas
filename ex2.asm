.MODEL SMALL
.STACK 100H
.DATA
    MATRIZ DB 4 DUP(4 DUP(?))
    MSG1 DB "DIGITE OS ELEMENTOS DA DIAGONAL PRINCIPAL: $"
.CODE


MAIN PROC
MOV AX, @DATA
MOV DS, AX

MOV CX, 4
XOR BX, BX
XOR SI, SI

MOV AH, 9 
LEA DX, MSG1
INT 21H

MOV AH, 1

LER: 
INT 21H
SUB AL, 30H

MOV MATRIZ[BX][SI], AL

ADD BX, 4
INC SI

LOOP LER

MOV AH, 2
MOV DL, 10 
INT 21H

IMPRIMIR_MATRIZ:
XOR SI, SI 
XOR BX, BX 

MOV CL, 4
MOV CH, 4
MOV AH, 02H

LINHA:
MOV DL, MATRIZ[BX + SI]
ADD DL, 30H
INT 21H

INC SI
CMP SI, 4
JE COLUNA
JMP LINHA

COLUNA:
MOV DL, 10
INT 21H

XOR SI,SI 
ADD BX, 4
CMP BX, 16
JE SAI
JMP LINHA

SAI:

MOV AH, 4CH
INT 21H


MAIN ENDP
END MAIN
